weather = 'colder'

case weather
when weather == 'sunny'
    puts "It puts the sunscreen on its skin!"
when weather == 'cloudy'
    puts "Wear a sweater in case it gets chilly."
when weather == 'rainy'
    puts "Bring an umbrella, ella, ella..."
when weather == 'cold'
    puts "ALL THE FLANNEL."
when weather == 'colder'
    puts "Just stay inside today. It's not like your car will start anyway!"
end
